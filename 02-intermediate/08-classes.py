class Animal:
    def __init__(self, nombre, peso):
        self.nombre = nombre
        self.peso = peso

    def comer(self):
        print(f"{self.nombre} está comiendo ")

    def dormir():
        print("El animal está durmiendo")


gato = Animal("michi", "5kg")
print(gato.nombre)
print(gato.peso)
gato.comer()
